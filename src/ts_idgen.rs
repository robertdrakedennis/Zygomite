/// `PascalCase` a `SCREAMING_SNAKE` identifier (`SLOT_GRID` -> `SlotGrid`).
fn pascal_case(name: &str) -> String {
    let mut out = String::with_capacity(name.len());
    for part in name.split('_').filter(|p| !p.is_empty()) {
        let mut chars = part.chars();
        if let Some(first) = chars.next() {
            out.extend(first.to_uppercase());
            out.extend(chars.flat_map(char::to_lowercase));
        }
    }
    out
}

fn ts_string(s: &str) -> String {
    let mut out = String::with_capacity(s.len() + 2);
    out.push('\'');
    for c in s.chars() {
        match c {
            '\'' => out.push_str("\\'"),
            '\\' => out.push_str("\\\\"),
            '\n' => out.push_str("\\n"),
            '\r' => out.push_str("\\r"),
            _ => out.push(c),
        }
    }
    out.push('\'');
    out
}

fn type_char_label(c: Option<u8>) -> String {
    match c {
        Some(b) if b.is_ascii_graphic() => ts_string(&(b as char).to_string()),
        Some(b) => format!("'\\u{b:04x}'"),
        None => "null".to_owned(),
    }
}

fn render_interfaces(resolved: &Resolved) -> String {
    let mut out = String::from(GENERATED_HEADER);
    out.push('\n');
    out.push_str("export const Interfaces = {\n");
    for iface in &resolved.interfaces {
        let _ = writeln!(out, "    {}: {},", iface.name, iface.id);
    }
    out.push_str("} as const;\n");

    for iface in &resolved.interfaces {
        if iface.components.is_empty() {
            continue;
        }
        let obj = pascal_case(&iface.name);
        let _ = write!(out, "\nexport const {obj} = {{\n    id: {},\n    com: {{\n", iface.id);
        for (cid, cname) in &iface.components {
            let _ = writeln!(out, "        {cname}: {cid},");
        }
        let _ = write!(
            out,
            "    }},\n    componentCount: {},\n}} as const;\n",
            iface.component_count
        );
    }
    out
}

fn render_simple(export_name: &str, pairs: &[(u32, String)]) -> String {
    let mut out = String::from(GENERATED_HEADER);
    let _ = write!(out, "\nexport const {export_name} = {{\n");
    for (id, name) in pairs {
        let _ = writeln!(out, "    {name}: {id},");
    }
    out.push_str("} as const;\n");
    out
}

fn render_varbits(resolved: &Resolved) -> String {
    let mut out = String::from(GENERATED_HEADER);
    out.push_str("\nexport const Varbits = {\n");
    for vb in &resolved.varbits {
        let _ = writeln!(out, "    {}: {},", vb.name, vb.id);
    }
    out.push_str("} as const;\n\n");
    out.push_str(
        "export const VarbitDefs: Record<number, { basevar: number; startbit: number; endbit: number }> = {\n",
    );
    for vb in &resolved.varbits {
        let _ = writeln!(
            out,
            "    {}: {{ basevar: {}, startbit: {}, endbit: {} }},",
            vb.id, vb.basevar, vb.startbit, vb.endbit
        );
    }
    out.push_str("};\n");
    out
}

fn render_enums(resolved: &Resolved) -> String {
    let mut out = String::from(GENERATED_HEADER);
    out.push_str("\nexport const Enums = {\n");
    for en in &resolved.enums {
        let _ = writeln!(out, "    {}: {},", en.name, en.id);
    }
    out.push_str("} as const;\n");

    for en in &resolved.enums {
        let e = &en.entry;
        let in_type = type_char_label(e.input_type_char);
        let out_type = type_char_label(e.output_type_char);
        let default = match &e.default {
            Some(crate::config::ScalarValue::Int(i)) => i.to_string(),
            Some(crate::config::ScalarValue::Long(l)) => l.to_string(),
            Some(crate::config::ScalarValue::Str(s)) => ts_string(s),
            None => "null".to_owned(),
        };
        let _ = write!(
            out,
            "\nexport const {}: {{ inType: string | null, outType: string | null, default: number | string | null, map: Record<number, number | string> }} = {{\n",
            en.name
        );
        let _ = writeln!(out, "    inType: {in_type},");
        let _ = writeln!(out, "    outType: {out_type},");
        let _ = writeln!(out, "    default: {default},");
        out.push_str("    map: {\n");
        let mut pairs: Vec<(i32, String)> = e
            .values
            .iter()
            .map(|p| {
                let v = match &p.value {
                    crate::config::ScalarValue::Int(i) => i.to_string(),
                    crate::config::ScalarValue::Long(l) => l.to_string(),
                    crate::config::ScalarValue::Str(s) => ts_string(s),
                };
                (p.key, v)
            })
            .collect();
        pairs.sort_by_key(|(k, _)| *k);
        for (k, v) in pairs {
            let _ = writeln!(out, "        {k}: {v},");
        }
        out.push_str("    },\n};\n");
    }
    out
}

/// Build the `(file_name, body)` pairs for every emitted module in a stable order.
fn render_all(resolved: &Resolved) -> Vec<(String, String)> {
    vec![
        ("interfaces.ts".to_owned(), render_interfaces(resolved)),
        (
            "varps.ts".to_owned(),
            render_simple("Varps", &resolved.varps),
        ),
        (
            "varcs.ts".to_owned(),
            render_simple("Varcs", &resolved.varcs),
        ),
        ("varbits.ts".to_owned(), render_varbits(resolved)),
        ("enums.ts".to_owned(), render_enums(resolved)),
        (
            "structs.ts".to_owned(),
            render_simple("Structs", &resolved.structs),
        ),
        (
            "scripts.ts".to_owned(),
            render_simple("Scripts", &resolved.scripts),
        ),
    ]
}

#[derive(Serialize)]
struct Manifest {
    schema: &'static str,
    counts: BTreeMap<&'static str, usize>,
    files: BTreeMap<String, String>,
}

fn build_manifest(resolved: &Resolved, files: &[(String, String)]) -> String {
    let mut counts: BTreeMap<&'static str, usize> = BTreeMap::new();
    counts.insert("interfaces", resolved.interfaces.len());
    counts.insert(
        "interface_components",
        resolved.interfaces.iter().map(|i| i.components.len()).sum(),
    );
    counts.insert("varps", resolved.varps.len());
    counts.insert("varcs", resolved.varcs.len());
    counts.insert("varbits", resolved.varbits.len());
    counts.insert("enums", resolved.enums.len());
    counts.insert("structs", resolved.structs.len());
    counts.insert("scripts", resolved.scripts.len());

    let mut hashes: BTreeMap<String, String> = BTreeMap::new();
    for (name, body) in files {
        hashes.insert(name.clone(), sha256_hex(body.as_bytes()));
    }
    let manifest = Manifest {
        schema: "ts-idgen/v1",
        counts,
        files: hashes,
    };
    let mut json = serde_json::to_string_pretty(&manifest).expect("manifest serializes");
    json.push('\n');
    json
}

/// Run the `generate-ts-ids` subcommand. Returns `true` when `--check` found
/// drift (the CLI maps that to exit code 3).
pub fn run(opts: &GenerateTsIdsOpts<'_>) -> Result<bool> {
    let resolved = resolve(opts)?;
    let mut files = render_all(&resolved);
    files.push(("manifest.json".to_owned(), build_manifest(&resolved, &files)));

    if opts.check {
        let mut drift: Vec<String> = Vec::new();
        for (name, body) in &files {
            let path = opts.out_dir.join(name);
            let on_disk = fs::read_to_string(&path).unwrap_or_default();
            if on_disk != *body {
                drift.push(name.clone());
            }
        }
        if drift.is_empty() {
            println!("generate-ts-ids --check: all {} file(s) up to date", files.len());
            return Ok(false);
        }
        println!("generate-ts-ids --check: {} file(s) differ:", drift.len());
        for name in &drift {
            println!("  - {name}");
        }
        return Ok(true);
    }

    fs::create_dir_all(opts.out_dir)
        .with_context(|| format!("failed creating {}", opts.out_dir.display()))?;
    for (name, body) in &files {
        let path = opts.out_dir.join(name);
        fs::write(&path, body)
            .with_context(|| format!("failed writing {}", path.display()))?;
        println!("wrote {}", path.display());
    }
    Ok(false)
}

