//! `generate-ts-ids` — join curated id→name maps with the runtime 948 pack,
//! validate every named id against the pack's real structure, and emit typed
//! TypeScript modules + a manifest the server imports.
//!
//! The cache cannot supply names (its `symbols/*.sym` are machine placeholders),
//! but it CAN supply existence and structure. This generator is the proof gate:
//! generation FAILS (exit 2) listing every violation if any named interface,
//! component, var, enum, struct, or script id is missing or structurally wrong
//! in the pack. Only the named subset is emitted; the guarantee comes from
//! generation-time validation, not from shipping the whole id universe.
//!
//! Read-only over the pack. Deterministic: ids sorted, no timestamps. The pack
//! files it reads are the single-file `.js5` containers under `--pack-root`,
//! addressed exactly as the `interfaces` / `varps` / `varbits` / config dump
//! subcommands address the flat cache (archive 3 interfaces, archive 2 config
//! groups for varp/varc/varbit, split archive 17 enums, split archive 22
//! structs, archive 12 scripts).

use crate::cache_bail;
use crate::config::{EnumEntry, parse_enum, parse_struct};
use crate::constants::{
    CONFIG_GROUP_VAR_BIT, CONFIG_GROUP_VAR_CLIENT, CONFIG_GROUP_VAR_PLAYER,
};
use crate::error::{Context, Result};
use crate::js5pack::PackArchive;
use crate::vars::{VarDomain, parse_var, parse_varbit};
use serde::Serialize;
use std::collections::BTreeMap;
use std::fmt::Write as _;
use std::fs;
use std::path::Path;

/// Split-archive id packing: enums use group << 8 | file, structs group << 5.
const ENUM_BIT_SHIFT: u32 = 8;
const STRUCT_BIT_SHIFT: u32 = 5;

const GENERATED_HEADER: &str = "// GENERATED by rs3-cache-rs `generate-ts-ids` from the runtime pack + data/names/910 — do not edit.\n// Regenerate: cd tools/rs3-cache-rs && cargo run --release -- generate-ts-ids\n";

/// Resolved options for the `generate-ts-ids` subcommand.
#[derive(Debug)]
pub struct GenerateTsIdsOpts<'a> {
    /// Runtime pack root holding the `client.*.js5` files.
    pub pack_root: &'a Path,
    /// Curated name maps directory (`<data-dir>/names/910` by default).
    pub names_dir: &'a Path,
    /// Output directory for the generated TS modules + manifest.
    pub out_dir: &'a Path,
    /// Compare-only: write nothing, return drift list (CLI maps to exit 3).
    pub check: bool,
}

