/// Check that the names within one domain are unique `SCREAMING_SNAKE`
/// identifiers and that no id repeats, pushing every violation onto `violations`.
fn check_uniqueness(
    domain: &str,
    pairs: &[(u32, &str)],
    violations: &mut Vec<String>,
) {
    let mut seen_names: BTreeMap<&str, u32> = BTreeMap::new();
    let mut seen_ids: BTreeMap<u32, &str> = BTreeMap::new();
    for &(id, name) in pairs {
        if let Some(prev) = seen_names.insert(name, id) {
            violations.push(format!(
                "{domain}: duplicate name {name} (ids {prev} and {id})"
            ));
        }
        if let Some(prev) = seen_ids.insert(id, name) {
            violations.push(format!(
                "{domain}: duplicate id {id} (names {prev} and {name})"
            ));
        }
    }
}

/// Build the resolved, validated dataset, or `Err` listing every violation.
struct Resolved {
    interfaces: Vec<ResolvedInterface>,
    varps: Vec<(u32, String)>,
    varcs: Vec<(u32, String)>,
    varbits: Vec<ResolvedVarbit>,
    enums: Vec<ResolvedEnum>,
    structs: Vec<(u32, String)>,
    scripts: Vec<(u32, String)>,
}

struct ResolvedInterface {
    id: u32,
    name: String,
    component_count: usize,
    components: Vec<(u32, String)>,
}

struct ResolvedVarbit {
    id: u32,
    name: String,
    basevar: u32,
    startbit: u8,
    endbit: u8,
}

struct ResolvedEnum {
    id: u32,
    name: String,
    entry: EnumEntry,
}

/// Look up a split-archive config entry (enum/struct) by reconstructed id.
fn split_group_file(id: u32, bit_shift: u32) -> (u32, u32) {
    (id >> bit_shift, id & ((1 << bit_shift) - 1))
}

/// Fetch one file's bytes from a legacy config group (archive 2, file = id).
fn legacy_config_file(
    config: &PackArchive,
    group: u32,
    id: u32,
) -> Result<Option<Vec<u8>>> {
    let Some(files) = config.group_files(group)? else {
        return Ok(None);
    };
    Ok(files.get(&id).cloned())
}

/// Fetch one file's bytes from a split config archive (enum/struct).
fn split_config_file(
    pack: &PackArchive,
    id: u32,
    bit_shift: u32,
) -> Result<Option<Vec<u8>>> {
    let (group, file) = split_group_file(id, bit_shift);
    let Some(files) = pack.group_files(group)? else {
        return Ok(None);
    };
    Ok(files.get(&file).cloned())
}

fn resolve(opts: &GenerateTsIdsOpts<'_>) -> Result<Resolved> {
    let interface_names = load_interfaces(opts.names_dir)?;
    let player_var_names = load_simple_map(opts.names_dir, "varps.json", "varps")?;
    let client_var_names = load_simple_map(opts.names_dir, "varcs.json", "varcs")?;
    let varbit_names = load_simple_map(opts.names_dir, "varbits.json", "varbits")?;
    let enum_names = load_simple_map(opts.names_dir, "enums.json", "enums")?;
    let struct_names = load_simple_map(opts.names_dir, "structs.json", "structs")?;
    let script_names = load_simple_map(opts.names_dir, "scripts.json", "scripts")?;

    let mut violations: Vec<String> = Vec::new();

    // Domain-level uniqueness checks (ids + names) before touching the pack.
    let iface_pairs: Vec<(u32, &str)> = interface_names
        .iter()
        .map(|i| (i.id, i.name.as_str()))
        .collect();
    check_uniqueness("interfaces", &iface_pairs, &mut violations);
    for iface in &interface_names {
        let comp_pairs: Vec<(u32, &str)> = iface
            .components
            .iter()
            .map(|(cid, n)| (*cid, n.as_str()))
            .collect();
        check_uniqueness(
            &format!("interface {} components", iface.id),
            &comp_pairs,
            &mut violations,
        );
    }
    for (domain, pairs) in [
        ("varps", &player_var_names),
        ("varcs", &client_var_names),
        ("varbits", &varbit_names),
        ("enums", &enum_names),
        ("structs", &struct_names),
        ("scripts", &script_names),
    ] {
        let p: Vec<(u32, &str)> = pairs.iter().map(|(id, n)| (*id, n.as_str())).collect();
        check_uniqueness(domain, &p, &mut violations);
    }

    // Open the packs once each (only those a seeded domain needs). Each config
    // archive is opened WITH its sibling `.patch.js5` merged, so an id present
    // only in the overlay patch (e.g. an overlay-added enum group) validates as
    // present — exactly as the runtime client loads it. `open_with_patch` is a
    // plain open when no patch file exists (e.g. scripts), so this is safe for
    // every archive.
    let interfaces_pack = PackArchive::open_with_patch(&opts.pack_root.join("client.interfaces.js5"))?;
    let config_pack = PackArchive::open_with_patch(&opts.pack_root.join("client.config.js5"))?;
    let enum_pack = PackArchive::open_with_patch(&opts.pack_root.join("client.enum.config.js5"))?;
    let struct_pack = PackArchive::open_with_patch(&opts.pack_root.join("client.struct.config.js5"))?;
    let scripts_pack = PackArchive::open_with_patch(&opts.pack_root.join("client.scripts.js5"))?;

    // Interfaces (archive 3): group = interface id, files = components.
    let mut interfaces = Vec::new();
    for iface in &interface_names {
        let Some(files) = interfaces_pack.group_files(iface.id)? else {
            violations.push(format!(
                "interface {} ({}) absent from archive 3",
                iface.id, iface.name
            ));
            continue;
        };
        let component_count = files.len();
        let mut components = Vec::with_capacity(iface.components.len());
        for (cid, cname) in &iface.components {
            if !files.contains_key(cid) {
                violations.push(format!(
                    "interface {} component {} ({}) has no file in the interface group",
                    iface.id, cid, cname
                ));
                continue;
            }
            if usize::try_from(*cid).is_ok_and(|c| c >= component_count) {
                violations.push(format!(
                    "interface {} component {} ({}) >= componentCount {component_count}",
                    iface.id, cid, cname
                ));
                continue;
            }
            components.push((*cid, cname.clone()));
        }
        interfaces.push(ResolvedInterface {
            id: iface.id,
            name: iface.name.clone(),
            component_count,
            components,
        });
    }

    // Varps (archive 2, group 60): file = id.
    let mut player_var_ids = Vec::new();
    for (id, name) in &player_var_names {
        match legacy_config_file(&config_pack, CONFIG_GROUP_VAR_PLAYER, *id)? {
            Some(bytes) => match parse_var(VarDomain::Player, *id, &bytes) {
                Ok(_) => player_var_ids.push((*id, name.clone())),
                Err(e) => violations.push(format!("varp {id} ({name}) failed to parse: {e}")),
            },
            None => violations.push(format!("varp {id} ({name}) absent from player var group")),
        }
    }

    // Varcs (archive 2, group 62): file = id.
    let mut client_var_ids = Vec::new();
    for (id, name) in &client_var_names {
        match legacy_config_file(&config_pack, CONFIG_GROUP_VAR_CLIENT, *id)? {
            Some(bytes) => match parse_var(VarDomain::Client, *id, &bytes) {
                Ok(_) => client_var_ids.push((*id, name.clone())),
                Err(e) => violations.push(format!("varc {id} ({name}) failed to parse: {e}")),
            },
            None => violations.push(format!("varc {id} ({name}) absent from client var group")),
        }
    }

    // Varbits (archive 2, group 69): file = id; record bit packing.
    let mut varbits = Vec::new();
    for (id, name) in &varbit_names {
        match legacy_config_file(&config_pack, CONFIG_GROUP_VAR_BIT, *id)? {
            Some(bytes) => match parse_varbit(*id, &bytes) {
                Ok(entry) => match (entry.base_var, entry.start_bit, entry.end_bit) {
                    (Some(basevar), Some(startbit), Some(endbit)) => varbits.push(ResolvedVarbit {
                        id: *id,
                        name: name.clone(),
                        basevar,
                        startbit,
                        endbit,
                    }),
                    _ => violations.push(format!(
                        "varbit {id} ({name}) missing basevar/startbit/endbit"
                    )),
                },
                Err(e) => violations.push(format!("varbit {id} ({name}) failed to parse: {e}")),
            },
            None => violations.push(format!("varbit {id} ({name}) absent from varbit group")),
        }
    }

    // Enums (split archive 17): record the decoded table.
    let mut enums = Vec::new();
    for (id, name) in &enum_names {
        match split_config_file(&enum_pack, *id, ENUM_BIT_SHIFT)? {
            Some(bytes) => match parse_enum(*id, &bytes) {
                Ok(entry) => enums.push(ResolvedEnum {
                    id: *id,
                    name: name.clone(),
                    entry,
                }),
                Err(e) => violations.push(format!("enum {id} ({name}) failed to parse: {e}")),
            },
            None => violations.push(format!("enum {id} ({name}) absent from archive 17")),
        }
    }

    // Structs (split archive 22): existence + parse only (params not embedded).
    let mut structs = Vec::new();
    for (id, name) in &struct_names {
        match split_config_file(&struct_pack, *id, STRUCT_BIT_SHIFT)? {
            Some(bytes) => match parse_struct(*id, &bytes) {
                Ok(_) => structs.push((*id, name.clone())),
                Err(e) => violations.push(format!("struct {id} ({name}) failed to parse: {e}")),
            },
            None => violations.push(format!("struct {id} ({name}) absent from archive 22")),
        }
    }

    // Scripts (archive 12): group present.
    let mut scripts = Vec::new();
    for (id, name) in &script_names {
        if scripts_pack.group_container(*id).is_some() {
            scripts.push((*id, name.clone()));
        } else {
            violations.push(format!("script {id} ({name}) absent from archive 12"));
        }
    }

    if !violations.is_empty() {
        violations.sort();
        let mut msg = format!(
            "generate-ts-ids: {} validation violation(s) against the pack:\n",
            violations.len()
        );
        for v in &violations {
            let _ = writeln!(msg, "  - {v}");
        }
        cache_bail!("{msg}");
    }

    Ok(Resolved {
        interfaces,
        varps: player_var_ids,
        varcs: client_var_ids,
        varbits,
        enums,
        structs,
        scripts,
    })
}

