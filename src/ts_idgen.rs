#[derive(Debug, serde::Deserialize)]
struct InterfaceNameEntry {
    name: String,
    #[serde(default)]
    components: BTreeMap<String, String>,
}

/// Parse `<id-string>` keys into `u32`, failing loudly on a non-numeric key.
fn parse_id(key: &str, domain: &str) -> Result<u32> {
    key.parse::<u32>()
        .with_context(|| format!("{domain} name map has non-numeric id key '{key}'"))
}

/// Read a simple `{ "<id>": "NAME" }` map, returning `(id, name)` pairs sorted by
/// id, or an empty list when the file is absent.
fn load_simple_map(names_dir: &Path, file: &str, domain: &str) -> Result<Vec<(u32, String)>> {
    let path = names_dir.join(file);
    if !path.is_file() {
        return Ok(Vec::new());
    }
    let text = fs::read_to_string(&path)
        .with_context(|| format!("failed reading {}", path.display()))?;
    let raw: BTreeMap<String, String> = serde_json::from_str(&text)
        .with_context(|| format!("failed parsing {}", path.display()))?;
    let mut out: Vec<(u32, String)> = Vec::with_capacity(raw.len());
    for (key, name) in raw {
        out.push((parse_id(&key, domain)?, name));
    }
    out.sort_by_key(|(id, _)| *id);
    Ok(out)
}

/// One named interface plus its named components, ids parsed and sorted.
struct InterfaceName {
    id: u32,
    name: String,
    components: Vec<(u32, String)>,
}

fn load_interfaces(names_dir: &Path) -> Result<Vec<InterfaceName>> {
    let path = names_dir.join("interfaces.json");
    if !path.is_file() {
        return Ok(Vec::new());
    }
    let text = fs::read_to_string(&path)
        .with_context(|| format!("failed reading {}", path.display()))?;
    let raw: BTreeMap<String, InterfaceNameEntry> = serde_json::from_str(&text)
        .with_context(|| format!("failed parsing {}", path.display()))?;
    let mut out = Vec::with_capacity(raw.len());
    for (key, entry) in raw {
        let id = parse_id(&key, "interfaces")?;
        let mut components: Vec<(u32, String)> = Vec::with_capacity(entry.components.len());
        for (ck, cname) in entry.components {
            components.push((
                parse_id(&ck, &format!("interfaces[{id}].components"))?,
                cname,
            ));
        }
        components.sort_by_key(|(cid, _)| *cid);
        out.push(InterfaceName {
            id,
            name: entry.name,
            components,
        });
    }
    out.sort_by_key(|i| i.id);
    Ok(out)
}

